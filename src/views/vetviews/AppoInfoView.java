    public void setAppoInfo(PseudoFile appoFile, 
        String appoId) throws IOException{
        
        HashMap<String,String> data = PseudoSearch.findRegisterByKey(
            appoFile, "idCita", appoId
        );
        
        String template = "<html>Fecha Inicio: %s <br>" +
            "Tipo de Cita: %s <br>" +
            "Cedula del vet: %s <br>" +
            "</html>";
        
        String text = String.format(template,
            data.get("empieza"), 
            data.get("tipoCita"),
            data.get("cedVet")
        );

        general_label.setText(text);

        setPetInfo(data.get("idMascota"));
    }
    
    private void setPetInfo(String petId) throws IOException{
        
        PseudoFile pseudoFile = new PseudoFile(
            new File("data/mascotas.csv"), 
            Pet.getColumns()
        );

        HashMap<String,String> data = PseudoSearch.findRegisterByKey(
            pseudoFile, "petId", petId
        );

        String template = "<html>Nombre: %s <br>" +
            "Raza: %s <br>" +
            "Color: %s <br>" +
            "</html>";

        String text = String.format(template,
            data.get("nombre"), 
            data.get("raza"),
            data.get("color")
        );

        pet_info_label1.setText(text);
        setClientInfo(data.get("clientCed"));

    }

    private void setClientInfo(String clientCed) throws IOException{
        
        PseudoFile pseudoFile = new PseudoFile(
            new File("data/clientes.csv"), 
            Client.getColumns()
        );

        HashMap<String,String> data = PseudoSearch.findRegisterByKey(
            pseudoFile, "cedula", clientCed
        );

        String template = "<html>Nombre: %s <br>" +
            "Telefono: %s <br>" +
            "Direccion: %s <br>" +
            "</html>";

        String text = String.format(template,
            data.get("nombre"), 
            data.get("telefono"),
            data.get("direccion")
        );

        client_info_label.setText(text);

    }


